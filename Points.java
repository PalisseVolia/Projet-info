public class Points {
 
	float i,j;
	public Points() {     					
	}

	public float getI() {         					//méthodes get et set
		return i;
	}
	public void setI(float i) {
		this.i = i;
	}
	public float getJ() {
		return j;
	}
	public void setJ(float j) {
		this.j = j;
	}
	
}